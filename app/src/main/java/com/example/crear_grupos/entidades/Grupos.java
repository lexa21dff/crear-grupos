package com.example.crear_grupos.entidades;

public class Grupos {

    private int id;
    private String nombre;
    private String ficha;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFicha() {
        return ficha;
    }

    public void setTelefono(String ficha) {
        this.ficha = ficha;
    }

}
